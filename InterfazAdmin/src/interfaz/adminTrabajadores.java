    public void mostrarTabla(){
        
        DefaultTableModel m = new DefaultTableModel();
        m.addColumn("NumeroEmpleado");
        m.addColumn("Puesto");
        m.addColumn("Nombre");
        m.addColumn("Apellido");
        m.addColumn("Telefono");
        m.addColumn("Fecha registro");
        tabla.setModel(m);
        
        String sql = "SELECT numeroEmpleado, puesto, nombre, apellido, telefono, fecha FROM trabajadores";
        
        String datos[]=new String[6];
        try{
            Conector.sentencia=Conector.con.createStatement();
            ResultSet r=Conector.sentencia.executeQuery(sql);
            while(r.next()){
                datos[0]=r.getString("numeroEmpleado");
                datos[1]=r.getString("puesto");
                datos[2]=r.getString("nombre");
                datos[3]=r.getString("apellido");
                datos[4]=r.getString("telefono");
                datos[5]=r.getString("fecha");
                m.addRow(datos);
            }
            tabla.setModel(m);
        }catch(SQLException ex){
            Logger.getLogger(adminTrabajadores.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

